    let currentLang = localStorage.getItem("captainsLang") || "ar";
    let currentStep = 1;

    function updateStageIndicator() {
      const indicator = document.getElementById("preview-stage-num");
      if (indicator) indicator.textContent = currentLang === "ar" ? `المرحلة 0${currentStep}` : `STAGE_0${currentStep}`;
    }

    function t(key) {
      if (Object.prototype.hasOwnProperty.call(translations[currentLang], key)) return translations[currentLang][key];
      return translations.en[key] || key;
    }

    function applyLanguage() {
      document.documentElement.lang = currentLang;
      document.documentElement.dir = currentLang === "ar" ? "rtl" : "ltr";
      
      // Select headers
      const titleText = currentLang === "ar" ? "استديو ذكالي | كابتينز" : "AI Studio | Captains Film";
      document.title = titleText;
      
      document.querySelectorAll("[data-logo-en][data-logo-ar]").forEach((logo) => {
        logo.src = currentLang === "ar" ? logo.dataset.logoAr : logo.dataset.logoEn;
        logo.alt = currentLang === "ar" ? "كابتينز" : "Captains Film";
      });
      document.querySelectorAll("[data-i18n]").forEach((node) => {
        node.textContent = t(node.dataset.i18n);
      });
      document.querySelectorAll("[data-lang-toggle]").forEach((button) => {
        button.textContent = currentLang === "ar" ? "EN" : "عربي";
        button.setAttribute("aria-label", currentLang === "ar" ? "Switch to English" : "التبديل إلى العربية");
      });
      updateStageIndicator();
    }

    const body = document.body;
    const hamburger = document.querySelector(".hamburger");
    const mobileMenu = document.querySelector(".mobile-menu");
    const contactModal = document.querySelector("#contactModal");

    function openModal(modal) {
      modal.classList.add("open");
      modal.setAttribute("aria-hidden", "false");
      body.classList.add("modal-open");
    }

    function closeModals() {
      document.querySelectorAll(".modal.open").forEach((modal) => {
        modal.classList.remove("open");
        modal.setAttribute("aria-hidden", "true");
      });
      body.classList.remove("modal-open");
    }

    hamburger.addEventListener("click", () => {
      const isOpen = body.classList.toggle("menu-open");
      hamburger.setAttribute("aria-expanded", String(isOpen));
      mobileMenu.setAttribute("aria-hidden", String(!isOpen));
    });

    document.querySelectorAll("[data-contact]").forEach((button) => {
      button.addEventListener("click", (event) => {
        event.preventDefault();
        body.classList.remove("menu-open");
        openModal(contactModal);
      });
    });

    document.querySelectorAll("[data-close]").forEach((button) => button.addEventListener("click", closeModals));
    document.querySelectorAll(".modal").forEach((modal) => {
      modal.addEventListener("click", (event) => {
        if (event.target === modal) closeModals();
      });
    });

    document.addEventListener("keydown", (event) => {
      if (event.key === "Escape") closeModals();
    });

    // INTERACTIVE SPOTLIGHT LIGHT TRACKER
    window.addEventListener("mousemove", (e) => {
      const x = e.clientX;
      const y = e.clientY;
      document.documentElement.style.setProperty("--mouse-x", `${x}px`);
      document.documentElement.style.setProperty("--mouse-y", `${y}px`);
    });

    // SCROLL REVEAL INTERSECTION OBSERVER
    const revealObserver = new IntersectionObserver((entries) => {
      entries.forEach(entry => {
        if (entry.isIntersecting) {
          entry.target.classList.add("revealed");
          revealObserver.unobserve(entry.target);
        }
      });
    }, {
      threshold: 0.08,
      rootMargin: "0px 0px -40px 0px"
    });

    document.querySelectorAll(".scroll-reveal").forEach(el => revealObserver.observe(el));

    // INTERACTIVE PIPELINE/WORKFLOW DASHBOARD
    document.querySelectorAll(".workflow-step-btn").forEach(btn => {
      btn.addEventListener("click", () => {
        const stepNum = btn.dataset.step;
        
        // Update active step selectors
        document.querySelectorAll(".workflow-step-btn").forEach(b => b.classList.remove("active"));
        btn.classList.add("active");
        
        // Update stage readout tag
        currentStep = Number(stepNum);
        updateStageIndicator();
        
        // Toggle preview tabs
        document.querySelectorAll(".preview-tab-content").forEach(tab => tab.classList.remove("active"));
        const targetTab = document.getElementById(`workflow-p${stepNum}`);
        if (targetTab) targetTab.classList.add("active");

        // Play the scene video in the active tab, pause the rest
        const allowMotion = !window.matchMedia("(prefers-reduced-motion: reduce)").matches;
        document.querySelectorAll(".preview-tab-content video").forEach((video) => {
          if (allowMotion && targetTab && targetTab.contains(video)) video.play().catch(() => {});
          else video.pause();
        });
      });
    });

    // LANGUAGE TOGGLE EVENT
    document.querySelectorAll("[data-lang-toggle]").forEach((button) => {
      button.addEventListener("click", () => {
        currentLang = currentLang === "ar" ? "en" : "ar";
        localStorage.setItem("captainsLang", currentLang);
        applyLanguage();
      });
    });

    // Pause decorative scene videos for users who prefer reduced motion;
    // otherwise play the active tab's scene while the panel is on screen
    if (window.matchMedia("(prefers-reduced-motion: reduce)").matches) {
      document.querySelectorAll(".pv-scene video").forEach((video) => {
        video.removeAttribute("autoplay");
        video.pause();
      });
    } else {
      const previewPanel = document.querySelector(".preview-box");
      if (previewPanel && "IntersectionObserver" in window) {
        const sceneObserver = new IntersectionObserver((entries) => {
          entries.forEach((entry) => {
            const activeVideo = document.querySelector(".preview-tab-content.active video");
            if (!activeVideo) return;
            if (entry.isIntersecting) activeVideo.play().catch(() => {});
            else activeVideo.pause();
          });
        }, { threshold: 0.15 });
        sceneObserver.observe(previewPanel);
      }
    }

    // INITIALIZATION
    applyLanguage();
    setTimeout(() => {
      body.classList.add("ready");
    }, 100);
